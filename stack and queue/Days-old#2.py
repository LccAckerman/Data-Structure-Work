def daysInMonth(year, month):
    if month in [1, 3, 5, 7, 8, 10, 12]:
        return 31
    elif month in [4, 6, 9, 11]:
        return 30
    else:
        if isLeapYear(year):
            return 29
        else:
            return 28


def isLeapYear(year):
    return year % 4 == 0 and year % 100 != 0

